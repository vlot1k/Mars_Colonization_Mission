from flask_restful import reqparse

put_parser = reqparse.RequestParser()
put_parser.add_argument('surname',         required=False, type=str)
put_parser.add_argument('name',            required=False, type=str)
put_parser.add_argument('age',             required=False, type=int)
put_parser.add_argument('position',        required=False, type=str)
put_parser.add_argument('speciality',      required=False, type=str)
put_parser.add_argument('address',         required=False, type=str)
put_parser.add_argument('email',           required=False, type=str)
put_parser.add_argument('hashed_password', required=False, type=str)

post_parser = reqparse.RequestParser()
post_parser.add_argument('surname',         required=True, type=str)
post_parser.add_argument('name',            required=True, type=str)
post_parser.add_argument('age',             required=True, type=int)
post_parser.add_argument('position',        required=True, type=str)
post_parser.add_argument('speciality',      required=True, type=str)
post_parser.add_argument('address',         required=True, type=str)
post_parser.add_argument('email',           required=True, type=str)
post_parser.add_argument('hashed_password', required=True, type=str)
